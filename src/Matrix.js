import React, { Component } from 'react';
import Cell from './Cell.js' //This line enables the app to work in the browser. So it seems that the CHILD gets imported into the PARENT.

export default class Matrix extends Component {

  genRow = (vals) => {
    console.log('This is vals:', vals);
    //return vals.map(val => <div className="cell"></div>) // replace me and render a cell component instead!
    return vals.map(val => <Cell value={val} />) // I guess this is how Matrix parents Cell.
  }

  genMatrix = () => {
    console.log('This is this.props.values:', this.props.values);
    return this.props.values.map(rowVals => <div className="row">{this.genRow(rowVals)}</div>)
  }

  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }

}

