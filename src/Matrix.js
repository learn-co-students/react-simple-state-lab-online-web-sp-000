import React, { Component } from 'react';
import Cell from './Cell.js'

export default class Matrix extends Component {
  
  makeRow = (vals) => (
       vals.map(colour => <Cell value={colour} />)
  )
  
  genMatrix = () => (
    this.props.values.map(rows => <div className="row">{this.makeRow(rows)}</div>)
  )
  
  render() {
    return (
      <div id="matrix">
        {this.genMatrix()}
      </div>
    )
  }
  
}



